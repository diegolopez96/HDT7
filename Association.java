public class Association<k,v> {
	//Variables
	private k ingles;
	private v espanol;
	
	public Association() {
		ingles = null;
		espanol = null;
	}
	
	//Set y gets de las variables
	public void setIngles(k string){
		ingles = string;
	}
	
	public void setEspanol(v cadena){	
		espanol = cadena;
	}
	
	public k getIngles(){	
		return ingles;
	}
	
	public v getEspanol(){	
		return espanol;
	}
        
	
}
